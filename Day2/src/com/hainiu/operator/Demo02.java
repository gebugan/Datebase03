public class Demo02 {
    public static void main(String[] args) {
        //++自增运算
        int i = 1;
        //++i
        i++;
        System.out.println(i);

        //参与操作 ++在后 先算后加
        int a = 10;
        int b = a++;
        System.out.println(a);
        System.out.println(b);























    }
}
