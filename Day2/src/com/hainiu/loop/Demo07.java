public class Demo07 {
    /**
     * do{
     *
     * }while()
     * 循环,不论条件是否成立，都先执行一次，在判断否进行下次巡回按
     */
    public static void main(String[] args) {

        int i = 1;
        do {
            System.out.println("hello world" +i );
            i++;

        }while (i <= 5);







    }



}
